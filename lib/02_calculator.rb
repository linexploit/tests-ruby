def add (num1, num2)
  result = num1 + num2
end

def subtract (num1, num2)
  result = num1 - num2
end

def sum (array)
  result = array.sum
end

def multiply (num1, num2)
  result = num1 * num2
end

def power (num1, num2)
  result = num1 ** num2
end

def factorial (num)
  result = 1 
  for i in 1..num
     result *= i
  end
  result
 end
 